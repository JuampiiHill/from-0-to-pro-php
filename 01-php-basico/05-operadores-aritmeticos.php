<?php

// Suma (+)
echo 5 + 6 . "\n";

// Resta (-)
echo 5 - 4 . "\n";

//Multiplicacion (*)
$multiplicacion = 2 * 2;
echo $multiplicacion . "\n";

// Division (/)
$division = 10 / 5;
echo $division . "\n";

// Exponenciacion - (**)
$resultado = 6 ** 4;
echo $resultado . "\n";

//Modulo (%) - Sirve para obtener el residuo de una division
$modulo = 5 % 2;
echo $modulo . "\n";

$horas = 7200;
echo "$horas segundos convertidos a hora son " . ($horas / 3600) . "\n";
echo "Sobran " . ($horas % 3600) . " segundos" . "\n";

echo "\n";